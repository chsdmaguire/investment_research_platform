Revenue: The amount of money received from the sale of products or services before taking into account any expenses. It is referred to as the “top line” and can also be called “Sales”. Occasionally, it is referred to as “net sales” to take into account items that are not expenses and reduce revenue such as returns, discounts, and allowances. 
