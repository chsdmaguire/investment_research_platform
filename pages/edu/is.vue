</li>
<li style="padding-top:2vh">
Research & Development (R&D): R&D expenses are related to the direct investments a company makes to develop new products or intellectual property that give it a competitive advantage over others. Companies are usually highly secretive about what they spend their R&D budget on, and it is a primary factor for a company’s long-term growth and success.
</li>
<li style="padding-top:2vh">
Depreciation & Amortization (D&A): These are non-cash expenses that reflect a reduction in long-term assets such as PP&E or Intangibles. For example, when a company buys a building for one million dollars, it expects to use the building for several years. Rather than recognizing the entire cost of the building in just the first year, it spreads out the costs of obtaining this building over time. So, if a company were to purchase this million-dollar building, and expected to use it for ten years, it would recognize an annual depreciation of $100,000. Amortization reflects this same concept but for intangible assets like patents or trademarks. 
</li>
**Please note, not all Income Statements are formatted the same and some may include various combinations of the above-mentioned expenses, or not have them on the Income Statement. For example, a company could have one line item be Sales, General, & Administrative. COGS and Revenue, however, will never be entangled with these other expenses. 
